package apple.npc.creation.from_scratch.player.info;

public class PlayerVarInfo {
    public String global;
    public int uid;
    public String name;
    public int value;

    public PlayerVarInfo(String global, int uid, String name, int value) {
        this.global = global;
        this.uid = uid;
        this.name = name;
        this.value = value;
    }
}
